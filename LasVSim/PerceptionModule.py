class Sensors(object):
    """
    Sensor Modal for Autonomous Car Simulation System
    a interface with ctypes for the dynamic library
    """
    def __init__(self, step_length, path=None):
        self.__step_length = step_length
        self.dll = CDLL(SENSORS_MODEL_PATH)
        from TrafficModule import MAX_TRAFFIC
        self.other_car_num = MAX_TRAFFIC
        other_car_arr = ObjectInfo * self.other_car_num
        detect_car_arr = ObjectInfoFromSensor * self.other_car_num
        self.other_cars = other_car_arr()
        self.detect_cars =detect_car_arr()

    def __del__(self):
        # FreeLibrary(self.dll._handle)
        del self.dll

    def setVehicleModel(self, vehicle_model):
        self.vehicle_model = vehicle_model

    def setSensor(self, sensors):
        self.sensors = sensors
        # print('Input to DLL: '),
        # for i in range(len(sensors)):
        #     print(sensors[i].accuracy_location),
        #     print(' '),
        # print()
        self.dll.SetSensors(len(sensors), byref(sensors))
        print('\nsensor configured')

    def update(self, pos, vehicles):
        x, y, velocity, a = pos
        for i in range(len(vehicles)):
            if not vehicles[i]['render']:
                self.other_cars[i].object_id_real = i
                self.other_cars[i].object_type_real = 0
                self.other_cars[i].object_x_real = 999999.9
                self.other_cars[i].object_y_real = 999999.9
                self.other_cars[i].object_heading_real = 0.0
                self.other_cars[i].object_width_real = 0.0
                self.other_cars[i].object_length_real = 0.0
                self.other_cars[i].object_height_real = 0.0
                self.other_cars[i].object_radius_real = 0.0
                self.other_cars[i].object_velocity_real = 0.0
            else:
                v = vehicles[i]
                self.other_cars[i].object_id_real = i
                self.other_cars[i].object_type_real = v['type']
                self.other_cars[i].object_x_real = v['x']
                self.other_cars[i].object_y_real = v['y']
                self.other_cars[i].object_heading_real = v['angle']
                h, w, x1, y1, img = self.vehicle_model.get_vehicle(v['type'])
                self.other_cars[i].object_width_real = w
                self.other_cars[i].object_length_real = h
                self.other_cars[i].object_height_real = 1.7
                self.other_cars[i].object_radius_real = 3.0
                self.other_cars[i].object_velocity_real = v['v']
        self.detected_object_num = self.dll.Detect(c_float(x), c_float(y),
                                                   c_float(a),
                                                   c_int(len(self.other_cars)),
                                                   byref(self.other_cars),
                                                   byref(self.detect_cars))

    def getVisibleVehicles(self):
        visible_vehicles = []
        for i in range(self.detected_object_num):
            vehicle = self.detect_cars[i]
            id = vehicle.object_id_detected
            type = vehicle.object_type_detected
            x = vehicle.object_x_detected
            y = vehicle.object_y_detected
            a = vehicle.object_heading_detected
            w = vehicle.object_width_detected
            l = vehicle.object_length_detected
            height = vehicle.object_height_detected
            radius = vehicle.object_radius_detected
            v = vehicle.object_velocity_detected
            if w < 1:
                w = 1.0
            if l < 1:
                l = 1.0
            visible_vehicles.append((id, x, y, v, a, w, l))
        return visible_vehicles


